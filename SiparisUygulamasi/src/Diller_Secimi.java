    public void labelDegistir() {
        String yazi = "Şu diller seçildi :";
        for (String dil : set) {
            yazi += dil + ", ";
        }
        diller_alani.setText(yazi);
    }
    private void gosterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gosterActionPerformed
        if (java_check.isSelected()) {
            set.add("Java");
        } else {
            set.remove("Java");
        }
        if (Python_check.isSelected()) {
            set.add("Python");
        } else {
            set.remove("Python");
        }
        if (C_check.isSelected()) {
            set.add("C++");
        } else {
            set.remove("C++");
        }
        labelDegistir();

    }//GEN-LAST:event_gosterActionPerformed

